function divShow() {
	let mobile = document.getElementById("navs")
	if(mobile.style.display = 'none'){
		mobile.style.display = 'block'
	}
}
function closed() {
	let mobile = document.getElementById("navs")
	if(mobile.style.display = 'block'){
		mobile.style.display = 'none'
	}
}
